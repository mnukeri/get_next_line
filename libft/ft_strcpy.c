#include "libft.h"

char	*ft_strcpy(char *dest, const char *src)
{
	size_t p;

	p = 0;
	while (src[p] != '\0')
	{
		dest[p] = src[p];
		p++;
	}
	dest[p] = '\0';
	return (dest);
}
