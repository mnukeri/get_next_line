#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	int p;
	int k;

	k = ft_strlen(s) + 1;
	p = 0;
	while (p < k)
	{
		if (s[p] == c)
			return ((char *)&s[p]);
		else
			p++;
	}
	return (0);
}
