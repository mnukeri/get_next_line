#include "libft.h"

void	ft_putstr_fd(char const *s, int fd)
{
	int p;

	if (s == NULL)
		return ;
	p = 0;
	while (s[p] != '\0')
	{
		write(fd, &s[p], 1);
		p++;
	}
}
