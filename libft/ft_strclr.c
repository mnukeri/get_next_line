#include "libft.h"

void	ft_strclr(char *str)
{
	int p;

	p = 0;
	if (str == NULL)
		return ;
	while (str[p] != '\0')
	{
		str[p] = '\0';
		p++;
	}
	str[p] = '\0';
}
